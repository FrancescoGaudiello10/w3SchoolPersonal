# Data type is an important concept.

# String
x = "Hello World"
print(x)
print(type(x))
print(20*"-")

# int
x = 10
print(x)
print(type(x))
print(20*"-")

# float
x = 20.5
print(x)
print(type(x))
print(20*"-")

# complex
x = 1j
print(x)
print(type(x))
print(20*"-")

# list
x = ["apple", "banana", "cherry"]
print(x)
print(type(x))
print(20*"-")

# tuple
x = ("apple", "banana", "cherry")
print(x)
print(type(x))
print(20*"-")

# range
x = range(6)
print(x)
print(type(x))
print(20*"-")

# dict
x = {"name": "John", "age": 36}
print(x)
print(type(x))
print(20*"-")

# set
x = {"apple", "banana", "cherry"}
print(x)
print(type(x))
print(20*"-")

# frozenset
x = frozenset({"apple", "banana", "cherry"})
print(x)
print(type(x))
print(20*"-")

# bool
x = True
print(x)
print(type(x))
print(20*"-")

# bytes
x = b"hello"
print(x)
print(type(x))
print(20*"-")

# bytearray
x = bytearray(5)
print(x)
print(type(x))
print(20*"-")

# memoryview
x = memoryview(bytes(5))
print(x)
print(type(x))
print(20*"-")

